public class Math { // membuat class Math
    double no1; // notegwh: proprty (variable untuk menampung data)
    double no2;
    double no3;

    // notegwh: ini adalah method "add, multiply, devide"
    public double add() {
        return no1 + no2 + no3;
    }

    public double multiply() {
        return no1 * no2 * no3;
    }

    public double devide() {
        return (no1 + no2) /  no3;
    }
}